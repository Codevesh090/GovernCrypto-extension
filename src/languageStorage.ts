/**
 * Language preference storage
 */

const LANGUAGE_KEY = 'selectedLanguage';

export async function saveLanguagePreference(languageCode: string): Promise<void> {
  await chrome.storage.local.set({ [LANGUAGE_KEY]: languageCode });
}

export async function getLanguagePreference(): Promise<string> {
  const result = await chrome.storage.local.get(LANGUAGE_KEY);
  return result[LANGUAGE_KEY] || 'en'; // Default to English
}

